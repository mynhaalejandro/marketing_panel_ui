<script type="text/javascript">

$(document).ready(function () {
  initialize_owl($('#news-slider1'));

  let tabs = [
    { target: '#home', owl: '#news-slider1' },
    { target: '#profile', owl: '#news-slider2' },
    { target: '#messages', owl: '#owl3' },
    { target: '#settings', owl: '#owl4' },
  ];

  // Setup 'bs.tab' event listeners for each tab
  tabs.forEach((tab) => {
    $(`a[href="${ tab.target }"]`)
      .on('shown.bs.tab', () => initialize_owl($(tab.owl)))
      .on('hide.bs.tab', () => destroy_owl($(tab.owl)));
  });    
});

function initialize_owl(el) {
  el.owlCarousel({
    items : 3,
        itemsDesktop:[1199,3],
        itemsDesktopSmall:[980,2],
        itemsMobile : [600,1],
        navigation:true,
        navigationText:["",""],
        pagination:true,
        autoPlay:true
  });
}

function destroy_owl(el) {
  el.data('owlCarousel').destroy();
}

</script>